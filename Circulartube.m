% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
     data_1_str = get(handles.edit1 ,'string');
     data_1_num = str2num(data_1_str);
     
     data_2_str = get(handles.edit2 ,'string');
     data_2_num = str2num(data_2_str);
     
     data_3_str = get(handles.edit3 ,'string');
     data_3_num = str2num(data_3_str);
     
     data_4_str = get(handles.edit4 ,'string');
     data_4_num = str2num(data_4_str);
     
     if get(handles.radiobutton2,'value')&get(handles.radiobutton4,'value')
         data_5_str = get(handles.edit9 ,'string');
         data_5_num = str2num(data_5_str);
         data_7_str = get(handles.edit11 ,'string');
         data_7_num = str2num(data_7_str);
     elseif get(handles.radiobutton2,'value')&get(handles.radiobutton5,'value')
         data_5_str = get(handles.edit9 ,'string');
         data_5_num = str2num(data_5_str);
         data_8_str = get(handles.edit12 ,'string');
         data_8_num = str2num(data_8_str);
         data_7_num=data_8_num*data_4_num;
     elseif get(handles.radiobutton3,'value')&get(handles.radiobutton4,'value')
         data_6_str = get(handles.edit10 ,'string');
         data_6_num = str2num(data_6_str);
         data_7_str = get(handles.edit11 ,'string');
         data_7_num = str2num(data_7_str);
         data_5_num=4*data_6_num/(pi*data_1_num*data_1_num)
     else
         data_6_str = get(handles.edit10 ,'string');
         data_6_num = str2num(data_6_str);
         data_8_str = get(handles.edit12 ,'string');
         data_8_num = str2num(data_8_str);
         data_5_num=4*data_6_num/(pi*data_1_num*data_1_num)
         data_7_num=data_8_num*data_4_num;
     end
     Re_num=(data_4_num*data_5_num*data_1_num)/data_7_num;
     Re_str=num2str(Re_num);
     set(handles.edit13, 'string', Re_str);
     A='层流';
     B='湍流(水力光滑)';
     C='湍流(混合摩擦)';
     D='湍流(水力粗糙)';
     temp=data_2_num*0.001/data_1_num;
     if Re_num<=2000
         x=64/Re_num;
         set(handles.edit14,'string',A);
     elseif Re_num>=3000&Re_num<=59.7*temp^(-8/7)
         x=0.3164*Re_num^(-0.25);
         set(handles.edit14,'string',B);
     elseif Re_num>59.7*temp^(-8/7)&Re_num<=(665-765*log10(temp))/temp
         x=1/(-1.8*log10(6.8/Re_num+(data_2_num/(3.7*data_1_num)^1.11)))^2;
         set(handles.edit14,'string',C);
     else
         x=(2*log10(3.7*data_1_num/data_2_num))^(-0.5);
         set(handles.edit14,'string',D);
     end
     y=x*data_3_num*data_5_num*data_5_num/(2*9.8*data_1_num);
     y_str=num2str(y);
     x_str=num2str(x);
     set(handles.edit15, 'string', x_str);
     set(handles.edit16, 'string', y_str);
     

